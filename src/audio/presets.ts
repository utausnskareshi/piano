// 100 instrument presets, indexed 0..99 (General MIDI-style ordering).
// Each preset compiles to a 32-element Float32Array (PARAMS_LEN in worklet/wasm).
//
// Param indices (must match wasm/src/lib.rs):
//   0  synth_type (0=subtractive, 1=fm2op, 2=karplus_strong, 3=additive3, 4=organ_drawbar)
//   1  osc1_wave (0..6: sine, saw, square, tri, noise, pulse25, pulse10)
//   2  osc2_wave
//   3  osc2_detune_semitones
//   4  osc_mix
//   5  noise_level
//   6  filter_cutoff_hz
//   7  filter_q
//   8  filter_env_amount_hz
//   9..12  amp ADSR (a, d, s, r) in seconds (s is level)
//   13..16 filter ADSR
//   17 fm_ratio
//   18 fm_index
//   19 ks_damping
//   20 ks_feedback
//   21 master_gain
//   22 lfo_rate_hz
//   23 lfo_pitch_semi
//   24 drive

export const PARAMS_LEN = 32;

