export const PRESETS: Preset[] = RAW.map(([name, category, params], i) => ({
  id: i,
  name,
  category,
  params: padToLen(params, PARAMS_LEN)
}));

function padToLen(arr: number[], len: number): number[] {
  const out = arr.slice(0, len);
  while (out.length < len) out.push(0);
  return out;
}

export const CATEGORIES = Array.from(new Set(PRESETS.map(p => p.category)));

export function presetParams(id: number): Float32Array {
  const p = PRESETS[Math.max(0, Math.min(PRESETS.length - 1, id))];
  return new Float32Array(p.params);
}
